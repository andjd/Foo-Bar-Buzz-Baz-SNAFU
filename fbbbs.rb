def fbbbs num
	foo = (num % 3 == 0)
	bar = (num % 5 == 0)
	buzz = (num % 11 == 0)

	if buzz && bar && foo
		return 'SNAFU'
	elsif (buzz && bar) || (buzz && foo)
		return 'fizzbuzz'
	elsif (foo && bar)
		return "baz"
	elsif buzz
		return "buzz"
	elsif bar
		return "bar"
	elsif foo
		return "foo"
	else
		return "nil"
	end
end

